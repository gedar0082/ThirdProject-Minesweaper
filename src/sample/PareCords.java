package sample;

public class PareCords {
    private int PointX;
    private int PointY;
    PareCords(int PointY, int PointX){
        this.PointX = PointX;
        this.PointY = PointY;
    }

    public int getX(){
        return PointX;
    }

    public int getY(){
        return PointY;
    }

}
